#Operadores aritméticos
resultado = 5 + 3 #suma
resultado = 5 - 3 #resta
resultado = 5 * 3 #multiplicación
resultado = 5 / 3 #división con decimales
print(resultado)  
resultado = 5 // 3 #división con números enteros
print(resultado)
resultado = 5 % 3 #módulo
resultado = 5 ** 3 #exponencial 5 elevado a la 3 en este caso

#Operadores de asignación
x = 10 #asignación simple
x += 5 #asignación suma
print(x)
x -= 3 #asignación resta
x *= 2 #asignación multiplicación
x /= 4 #asignación división
x %= 5 #asignación módulo
